#include "libft.h"

char	*ft_itoa(int n)
{
	char *str;

	str = (char *)malloc(sizeof(char) * n + 1);
	if (!str)
		return (NULL);
	if (str[0] == '-')
	{
		str[0] = '-';
	}
	else if(str[0] == '+')
	{
		str[0] = '+';
	}
	return (str);
}
