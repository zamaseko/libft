char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int a;
	int b;

	a = 0;
	while (s1[a] != '\0')
		a++;
	b = 0;
	while (s2[b] != '\0')
	{
		s1[a] = s2[b];
		a++;
		b++;
	}
	s1[a] = '\0';
	return (s1);
//
//	return(strncat(s1, s2, strlen(2))
}

