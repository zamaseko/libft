#include "libft.h"

int	ft_spaces(const char *c, unsigned int i)
{
	while (c[i] == ' ' || c[i] == '\t' || c[i] == '\r' ||
		c[i] == '\f' || c[i] == '\n' || c[i] == '\v')
		i++;
	return (i);
}
